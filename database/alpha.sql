create database alpha;
use alpha;

create table produtos_tbl(
	pro_id int not null primary key auto_increment,
    pro_nome varchar(155),
    pro_estoque int,
    pro_preco double,
    pro_categoria varchar(55)
)engine=InnoDB;

create table fornecedores_tbl(
	for_id int not null primary key auto_increment,
    for_nome varchar(155),
    for_cidade varchar(55),
    pro_cnpj char(18)
)engine=InnoDB;

create table pedido_tbl(
	ped_id int not null primary key auto_increment,
    pro_id int not null,
    for_id int not null,
    ped_data date,
    ped_quantidade int
)engine=InnoDB;

